public class Ejercicio369 {

    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n;

        n = sc.nextInt();

        while (n != 0) {

            for (int i = 0; i < n; i++) {

                System.out.print("1");

            }

            System.out.println();

            n = sc.nextInt();
        }
    }
}
